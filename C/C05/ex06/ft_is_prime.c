int	ft_is_prime(int nb)
{
	int	count;

	if (nb < 0)
		return (0);
	if (!nb || nb == 1)
		return (0);
	count = 2;
	while (count <= nb / 2)
	{
		if (!(nb % count))
			return (0);
		count++;
	}
	return (1);
}

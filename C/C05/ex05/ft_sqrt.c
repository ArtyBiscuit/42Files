int	ft_sqrt(int nb)
{
	int	a;
	int	b;

	a = 1;
	b = 0;
	while (nb <= 2147395600 && nb > 0)
	{
		if (b < nb)
			b = a * a;
		else if (b == nb)
			return (a - 1);
		else
			return (0);
		a++;
	}
	return (0);
}

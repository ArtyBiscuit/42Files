#include<unistd.h>

void	ft_is_negative(int n)
{
	char	nop;
	char	good;

	nop = 'N';
	good = 'P';
	if (n < 0)
	{
		write(1, &nop, 1);
	}
	else
	{
		write(1, &good, 1);
	}
}

char	*ft_strlowcase(char *str)
{
	int	cmp;

	cmp = 0;
	while (str[cmp] != '\0')
	{
		if (str[cmp] >= 65 && str[cmp] <= 90)
		{
			str[cmp] = str[cmp] + 32;
		}
		cmp++;
	}
	return (str);
}

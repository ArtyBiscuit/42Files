int	ft_str_is_numeric(char *str)
{
	int	cmp;

	cmp = 0;
	while (str[cmp] != '\0')
	{
		if (str[cmp] >= '0' && str[cmp] <= '9')
		{
			cmp++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}

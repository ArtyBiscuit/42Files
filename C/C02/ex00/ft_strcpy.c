char	*ft_strcpy(char *dest, char *src)
{
	int	cmp;

	cmp = 0;
	while (src[cmp] != '\0')
	{
		dest[cmp] = src[cmp];
		cmp++;
	}
	dest[cmp] = '\0';
	return (dest);
}

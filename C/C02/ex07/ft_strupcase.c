char	*ft_strupcase(char *str)
{
	int	cmp;

	cmp = 0;
	while (str[cmp] != '\0')
	{
		if (str[cmp] >= 97 && str[cmp] <= 122)
		{
			str[cmp] = str[cmp] - 32;
		}
		cmp++;
	}
	return (str);
}

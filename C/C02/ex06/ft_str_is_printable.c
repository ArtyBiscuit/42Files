int	ft_str_is_printable(char *str)
{
	int	cmp;

	cmp = 0;
	while (str[cmp] != '\0')
	{
		if (str[cmp] >= 32 && str[cmp] <= 126)
		{
			cmp++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
